void main() {
  int a = 10;
  int b = 3;

  print('a + b = ${a + b}');
  print('a - b = ${a - b}');
  print('a * b = ${a * b}');
  print('a / b = ${a / b}');
  print('a ~/ b = ${a ~/ b}');
  print('a % b = ${a % b}');

  print('a = $a');
  print('a++ = ${a++}');
  print('++a = ${++a}');
  print('a-- = ${a--}');
  print('--a = ${--a}');
}
