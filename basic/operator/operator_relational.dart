void main() {
  int a = 3, b = 4;
  print('$a == $b: ${a == b}');
  print('$a != $b: ${a != b}');
  print('$a > $b: ${a > b}');
  print('$a >= $b: ${a >= b}');
  print('$a < $b: ${a < b}');
  print('$a <= $b: ${a <= b}');

  String str1 = "123", str2 = '123', str3 = '456';
  print('str1 == str2: ${str1 == str2}');
  print('str1 == str3: ${str1 == str3}');
}
